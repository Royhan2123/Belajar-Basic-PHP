<?php

/**
 * Increment Dan Decrement.
 * sama seperti penggunaan bahasa pemograman lain
 * contoh nya seperti berikut :
 */

// salah satu contoh penggunaan increment;
$a = 0;
var_dump(++$a); // <- otomatis akan mengembalikan nilai $a yaitu 0,dan menaikkan 1 angka;
// atau

var_dump($a++); // <- sedangkan ini ialah post increment

$b = 1;
// salah satu contoh penggunaan decrement;
var_dump(--$b); // <- otomatis akan mengembalikan nilai $b yaitu 1,dan menurunkan 1 angka;
// atau
var_dump($b--);


